    private void tbl_empleadoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbl_empleadoKeyReleased
        // TODO add your handling code here:
        selec_datos();
        if (txt_codigo.getText().matches("[J]{1}[0-9]{3}")){
            lbl_codigo.setForeground(Color.black);
            lbl_codigo.setText("Codigo");
        }else{
            lbl_codigo.setForeground(Color.red);
            lbl_codigo.setText("Error (J###)");
        }
        if(txt_nombre.getText().matches("[A-Z]{1}[a-z]{2,9}([ ][A-Z]{1}[a-z]{2,9})?")){
            lbl_nombres.setForeground(Color.black);
            lbl_nombres.setText("Nombres");
        }else{
            lbl_nombres.setForeground(Color.red);
            lbl_nombres.setText("Error en nombres");
        }
        if(txt_apellido.getText().matches("[A-Z]{1}[a-z]{2,9}([ ][A-Z]{1}[a-z]{2,9})?")){
            lbl_apellidos.setForeground(Color.black);
            lbl_apellidos.setText("Apellido");
            
        }else{
            lbl_apellidos.setForeground(Color.red);
            lbl_apellidos.setText("Error en apellidos");
        }
        if(txt_telefono.getText().matches("[0-9]{8}")){
            lbl_telefono.setForeground(Color.black);
            lbl_telefono.setText("Telefono");
        }else{
            lbl_telefono.setForeground(Color.red);
            lbl_telefono.setText("Error en telefono");
        }
        if(txt_fNacimiento.getText().matches("[0-9]{4}[-]{1}[0-1]{1}[0-9]{1}[-]{1}[0-3]{1}[0-9]{1}")){
            lbl_fNacimiento.setForeground(Color.black);
            lbl_fNacimiento.setText("Fecha de nacimiento");
        }else{
            lbl_fNacimiento.setForeground(Color.red);
            lbl_fNacimiento.setText("Error en FN");
        }
        validar();
    }//GEN-LAST:event_tbl_empleadoKeyReleased

