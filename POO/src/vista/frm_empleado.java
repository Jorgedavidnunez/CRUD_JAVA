    public void validar(){
        if(txt_codigo.getText().isEmpty()){
            error_codigo.setText("Obligatorio");
        }else{
            error_codigo.setText("");
        }
        if(txt_nombre.getText().isEmpty()){
            error_nombres.setText("Obligatorio");
        }else{
            error_nombres.setText("");
        }
        if(txt_apellido.getText().isEmpty()){
            error_apellidos.setText("Obligatorio");
        }else{
            error_apellidos.setText("");
        }
        if(txt_direccion.getText().isEmpty()){
            error_direccion.setText("Obligatorio");
        }else{
            error_direccion.setText("");
        }
        if(txt_telefono.getText().isEmpty()){
            error_telefono.setText("Obligatorio");
        }else{
            error_telefono.setText("");
        }
        if(txt_fNacimiento.getText().isEmpty()){
            error_fNacimiento.setText("Obligatorio");
        }else{
            error_fNacimiento.setText("");
        }
        
        if(txt_codigo.getText().isEmpty()||lbl_codigo.getText().equals("Error (J###)")||txt_nombre.getText().isEmpty()
                ||lbl_nombres.getText().equals("Error en nombres")||txt_apellido.getText().isEmpty()||lbl_nombres.getText().equals("Error apellidos")
                ||txt_direccion.getText().isEmpty()||txt_telefono.getText().isEmpty()||lbl_telefono.getText().equals("Error en telefono")
                ||txt_fNacimiento.getText().isEmpty()||lbl_fNacimiento.getText().equals("Error en FN")){
            btn_aceptar.setEnabled(false);
            btn_modificar.setEnabled(false);
            btn_eliminar.setEnabled(false);
            lbl_aviso.setForeground(Color.red);
            lbl_aviso.setText("<html>Llene los campos correctamente para habilitar los botones<html>");
        }
        else{
            btn_aceptar.setEnabled(true);
            btn_modificar.setEnabled(true);
            btn_eliminar.setEnabled(true);
            lbl_aviso.setText("");
        }
    }

